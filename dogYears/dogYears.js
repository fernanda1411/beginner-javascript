/* Dog Years
With your knowledge of math operators and variables, 
use JavaScript to convert your age into dog years.*/

let myAge = 31;
let firstYears = 2 * 10.5;
let laterYears = (myAge - 2) * 4;
let myAgeInDogYears = firstYears + laterYears;

console.log('My age in dog years is ' + myAgeInDogYears);